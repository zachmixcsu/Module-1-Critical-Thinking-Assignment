print("This program will ask for two numbers and then will print the product and quotient of those numbers")
num1 = float(input("Please enter the first number: "))
num2 = float(input("Please enter the second number: "))


product = num1 * num2
quotient = num1 / num2

print(f'The product of the two numbers is {product}.')
print(f'The quotient of the two numbers is {quotient}.')

